#!/usr/bin/env node
// Writes src/build-target.ts with the BUILD_TARGET constant before tsc runs.
// Usage: node scripts/set-build-target.js developer
//        node scripts/set-build-target.js store

const fs = require('fs');
const path = require('path');

const target = process.argv[2];
if (target !== 'developer' && target !== 'store') {
  console.error('Usage: set-build-target.js <developer|store>');
  process.exit(1);
}

const out = path.join(__dirname, '..', 'src', 'build-target.ts');
fs.writeFileSync(out, `// Auto-generated by scripts/set-build-target.js — do not edit manually\nexport const BUILD_TARGET = "${target}" as const;\n`);
console.log(`BUILD_TARGET set to "${target}"`);
