#!/usr/bin/env node
// Writes src/build-target.ts with the BUILD_TARGET (and, for store builds,
// STORE_VARIANT) constants before tsc runs.
// Usage: node scripts/set-build-target.js developer
//        node scripts/set-build-target.js store [b1|b2]

const fs = require('fs');
const path = require('path');

const target = process.argv[2];
const variantArg = process.argv[3];
if (target !== 'developer' && target !== 'store') {
  console.error('Usage: set-build-target.js <developer|store> [b1|b2]');
  process.exit(1);
}
if (target === 'store' && variantArg !== undefined && variantArg !== 'b1' && variantArg !== 'b2') {
  console.error('Usage: set-build-target.js store [b1|b2]');
  process.exit(1);
}
const storeVariant = target === 'store' ? (variantArg || 'b1') : 'none';

const out = path.join(__dirname, '..', 'src', 'build-target.ts');
const contents = [
  '// Auto-generated by scripts/set-build-target.js -- do not edit manually',
  '// STORE_VARIANT is build tooling, test-expectation-selection, and evidence-',
  '// labeling metadata ONLY. Runtime code must never branch on it for',
  '// capability decisions -- use getExtractionCapabilities() (installed',
  '// manifest + API presence) from src/extract/capabilities.ts instead.',
  `export const BUILD_TARGET = "${target}" as const;`,
  `export const STORE_VARIANT = "${storeVariant}" as const;`,
  '',
].join('\n');
fs.writeFileSync(out, contents);
console.log(`BUILD_TARGET set to "${target}"${target === 'store' ? ` (STORE_VARIANT "${storeVariant}")` : ''}`);
